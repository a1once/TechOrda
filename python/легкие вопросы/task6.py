def calc_deposit(n, k, b):
    if not (0 <= n <= 10000):
        return "Ошибка: количество месяцев должно быть от 0 до 10000."
    if not (0 <= k <= 100):
        return "Ошибка: процентная ставка должна быть от 0 до 100."
    if not (0 <= b <= 10000):
        return "Ошибка: начальный баланс должен быть от 0 до 10000."
    
    for _ in range(n):
        b += b * (k / 100)
    return b

n = int(input("Введите количество месяцев (n): "))
k = float(input("Введите процентную ставку (k): "))
b = float(input("Введите начальный баланс (b): "))

print(calc_deposit(n, k, b))